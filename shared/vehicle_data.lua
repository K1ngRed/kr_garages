-- Clasificar tipo de vehiculo por clase GTA V
local function getTypeByClass(class)
    if class == 14 then return 'boat' end
    if class == 15 or class == 16 then return 'air' end
    -- Motos (8) y bicicletas (13) van con los autos
    return 'car'
end

-- Generar nombre legible desde spawn name
local function generateLabel(name)
    if not name then return 'Unknown Vehicle' end
    local cleanName = name:gsub('%d+$', '')
    local label = cleanName:gsub("^%l", string.upper):gsub("_(.)", function(c) 
        return " " .. string.upper(c) 
    end)
    return label
end

-- Funcion para registrar un vehiculo dinamicamente
function VehicleData.RegisterVehicle(modelHash, modelName, vehicleClass)
    if not modelHash or not modelName then return false end
    if VehicleData.Vehicles[modelHash] then return false end
    
    local vehType = getTypeByClass(vehicleClass or 0)
    VehicleData.Vehicles[modelHash] = {
        name = string.lower(modelName),
        type = vehType,
        class = vehicleClass or 0,
        label = generateLabel(modelName)
    }
    return true
end

-- Funcion para contar vehiculos registrados
function VehicleData.Count()
    local count = 0
    for _ in pairs(VehicleData.Vehicles) do count = count + 1 end
    return count
end

-- Funcion para obtener datos del vehiculo por hash
function VehicleData.GetByHash(hash)
    if type(hash) ~= 'number' then return nil end
    return VehicleData.Vehicles[hash]
end

-- Funcion para obtener datos del vehiculo por nombre
function VehicleData.GetByName(name)
    if type(name) ~= 'string' then return nil end
    local searchName = string.lower(name)
    for hash, data in pairs(VehicleData.Vehicles) do
        if data.name == searchName then return data end
    end
    return nil
end

-- Funcion para obtener el tipo de vehiculo
function VehicleData.GetVehicleType(modelNameOrHash)
    local data = nil
    if type(modelNameOrHash) == 'number' then
        data = VehicleData.GetByHash(modelNameOrHash)
    elseif type(modelNameOrHash) == 'string' then
        data = VehicleData.GetByName(modelNameOrHash)
    end
    
    if data then return data.type end
    
    -- Fallback: detectar por nombre si contiene palabras clave
    if type(modelNameOrHash) == 'string' then
        local lower = string.lower(modelNameOrHash)
        if lower:find('heli') or lower:find('jet') or lower:find('plane') or 
           lower:find('lazer') or lower:find('buzzard') or lower:find('savage') then
            return 'air'
        end
        if lower:find('boat') or lower:find('dinghy') or lower:find('seashark') or
           lower:find('speeder') or lower:find('marquis') then
            return 'boat'
        end
        -- Motos van con los autos (tipo 'car')
    end
    return 'car'
end

-- Funcion para convertir hash a nombre
function VehicleData.HashToName(hash)
    local data = VehicleData.GetByHash(hash)
    if data then return data.name end
    return nil
end

-- Funcion para validar si un vehiculo puede estar en un garaje de cierto tipo
function VehicleData.CanStoreInGarage(vehicleModelOrHash, garageType)
    local vehicleType = VehicleData.GetVehicleType(vehicleModelOrHash)
    if not garageType or garageType == '' then return true end
    
    return vehicleType == garageType
end

